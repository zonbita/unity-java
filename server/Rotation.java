public class Rotation {
    String x;
    String y;
    String z;
    String w;

    public Rotation(){
        x = "0";
        y = "0";
        z = "0";
        w = "0";
    }

    public Rotation(String x, String y, String z, String w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}
